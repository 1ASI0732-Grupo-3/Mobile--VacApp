class Animal {
  final int id;
  final String name;
  final String gender;
  final String birthDate;
  final String breed;
  final String location;
  final String bovineImg;
  final int stableId;

  const Animal({
    required this.id,
    required this.name,
    required this.gender,
    required this.birthDate,
    required this.breed,
    required this.location,
    required this.bovineImg,
    required this.stableId,
  });
}